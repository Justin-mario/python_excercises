# finding 7% return on investment after 10, 20, 30 years.

principal = 1000.00
annual_rate_of_return = 0.07

expected_amount_after_10_years = principal * (1 + annual_rate_of_return) ** 10
expected_amount_after_20_years = principal * (1 + annual_rate_of_return) ** 20
expected_amount_after_30_years = principal * (1 + annual_rate_of_return) ** 30

print("Total expected Amount After 10 years is: ", expected_amount_after_10_years)
print("Total expected Amount After 20 years is: ", expected_amount_after_20_years)
print("Total expected Amount After 30 years is: ", expected_amount_after_30_years)
